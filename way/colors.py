#!/usr/bin/env python

def ColorGenerator():
    colors = [
        '#a6cee3',
        '#1f78b4',
        '#b2df8a',
        '#33a02c',
        '#fb9a99',
        '#e31a1c',
        '#fdbf6f',
        '#ff7f00',
        '#cab2d6',
        '#6a3d9a',
        '#ffff99',
        '#b15928'
    ]

    i = 0

    while i < len(colors):
        yield colors[i]

        i += 1
